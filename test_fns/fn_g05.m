function f = fn_g05( x )
    f = 3 * x( 1 ) + 0.000001 * x( 1 ) ^ 3 + 2 * x( 2 ) + ( 0.000002 / 3 ) * x( 2 ) ^ 3;
    g1 = -x( 4 ) + x( 3 ) - 0.55;
    g2 = -x( 3 ) + x( 4 ) - 0.55;
    g3 = 1000 * sin( -x( 3 ) - 0.25) + 1000 * sin( -x( 4 ) - 0.25 ) + 894.8 - x( 1 );
    g4 = 1000 * sin( x( 3 ) - 0.25) + 1000 * sin( x( 3 ) - x( 4 ) - 0.25) + 894.8 - x( 2 );
    g5 = 1000 * sin( x( 4 ) - 0.25) + 1000 * sin( x( 4 ) - x( 3 ) - 0.25) + 1294.8;
    f = [ f -g1 -g2 -g3 -g4 -g5 ];
end